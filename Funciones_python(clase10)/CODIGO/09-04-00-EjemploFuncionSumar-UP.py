print('*** Función sumar ***')

# Definimos la funcion
def sumar(a, b):
    resultado_suma = a + b
    return resultado_suma

# Llamar a la funcion
resultado_funcion = sumar(8, 5)
print(f'Resultado función sumar: {resultado_funcion}')

resultado_funcion = sumar(9, 15)
print(f'Resultado función sumar: {resultado_funcion}')
